package com.cesarbassani.apprest.services;

import com.cesarbassani.apprest.domain.Pedido;
import com.cesarbassani.apprest.repositories.PedidoRepository;
import com.cesarbassani.apprest.services.exceptions.ObjectNotFoundException;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.Optional;

@Service
public class PedidoService {

    @Autowired
    private PedidoRepository pedidoRepository;

    public Pedido buscar(Integer id) {
        Optional<Pedido> obj = pedidoRepository.findById(id);
        return obj.orElseThrow(() -> new ObjectNotFoundException(
                "Objeto n√£o encontrado! Id: " + id + ", Tipo: " + Pedido.class.getName()));
    }
}
